export function getLang() {
  return localStorage.getItem("wcp_lang") || "en";
}

export function setLang(code) {
  localStorage.setItem("wcp_lang", code);
  window.dispatchEvent(new Event("wcp_lang_change"));
}

export function useTranslation() {
  const [lang, setLangState] = React.useState(getLang());
  React.useEffect(() => {
    const handler = () => setLangState(getLang());
    window.addEventListener("wcp_lang_change", handler);
    return () => window.removeEventListener("wcp_lang_change", handler);
  }, []);
  return { lang, setLang, tr: t[lang] || t.en, dir: LANGUAGES.find(l => l.code === lang)?.dir || "ltr" };
}

